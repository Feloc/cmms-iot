// ---- Timestamps: reglas de secuencia ----
private readonly TS_ORDER = [
  'takenAt',
  'arrivedAt',
  'checkInAt',
  'activityStartedAt',
  'activityFinishedAt',
  'deliveredAt',
] as const;

