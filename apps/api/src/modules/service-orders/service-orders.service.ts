  private calcDurationMin(a?: Date | null, b?: Date | null): number | null {
    if (!a || !b) return null;
    const ms = b.getTime() - a.getTime();
    if (!isFinite(ms) || ms < 0) return null;
    return Math.round(ms / 60000);
  }

  private buildOperationalTimes(so: any) {
    const visitMode = this.getVisitMode((so as any)?.formData ?? null);
    const isFollowUp = visitMode === 'FOLLOW_UP';

    const takenAt = so.takenAt ? new Date(so.takenAt) : null;
    const arrivedAt = so.arrivedAt ? new Date(so.arrivedAt) : null;
    const checkInAt = so.checkInAt ? new Date(so.checkInAt) : null;
    const activityStartedAt = so.activityStartedAt ? new Date(so.activityStartedAt) : null;
    const activityFinishedAt = so.activityFinishedAt ? new Date(so.activityFinishedAt) : null;
    const deliveredAt = so.deliveredAt ? new Date(so.deliveredAt) : null;

    const seg = (key: string, label: string, a: Date | null, b: Date | null) => {
      const durationMin = this.calcDurationMin(a, b);
      return {
        key,
        label,
        start: a ? a.toISOString() : null,
        end: b ? b.toISOString() : null,
        durationMin,
      };
    };

    return {
      segments: [
        seg('taken_arrived', 'Desplazamiento (takenAt → arrivedAt)', isFollowUp ? null : takenAt, isFollowUp ? null : arrivedAt),
        seg('arrived_checkin', 'Proceso de ingreso (arrivedAt → checkInAt)', isFollowUp ? null : arrivedAt, isFollowUp ? null : checkInAt),
        seg('checkin_start', 'Entrega del equipo (checkInAt → activityStartedAt)', isFollowUp ? null : checkInAt, activityStartedAt),
        seg('start_finish', 'Trabajo en sitio (activityStartedAt → activityFinishedAt)', activityStartedAt, activityFinishedAt),
        seg('finish_delivered', 'Entrega final (activityFinishedAt → deliveredAt)', activityFinishedAt, deliveredAt),
        seg('arrived_delivered', 'Duración del servicio (arrivedAt → deliveredAt)', isFollowUp ? null : arrivedAt, deliveredAt),
      ],
    };
  }

  private toMoney(v: any): number {
    const n = Number(v ?? 0);
    if (!Number.isFinite(n)) return 0;
    return Math.round((n + Number.EPSILON) * 100) / 100;
  }

  private normalizeQuoteCurrency(v: any): string {
    const c = String(v ?? 'COP').trim().toUpperCase();
    if (!c) return 'COP';
    if (c.length < 3 || c.length > 6) return 'COP';
    return c;
  }

  private getQuotesFromFormData(formData: any): any[] {
    const fd = formData && typeof formData === 'object' ? (formData as any) : {};
    const arr = Array.isArray(fd?._quotes) ? (fd._quotes as any[]) : [];
    return arr
      .filter((q) => q && typeof q === 'object' && q.id)
      .sort((a, b) => {
        const av = Number(a?.version ?? 0);
        const bv = Number(b?.version ?? 0);
        if (av !== bv) return bv - av;
        const ad = String(a?.createdAt ?? '');
        const bd = String(b?.createdAt ?? '');
        return bd.localeCompare(ad);
      });
  }

